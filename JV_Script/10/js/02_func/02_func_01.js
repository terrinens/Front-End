function printMsg (name, age) {
    // body에 출력하라
    document.write ("학생 이름은 : <b>" + name + "<br>");
    document.write ("학생 나이는 : <b>" + age + " <br>");
}

function printMsg2 (name, age) {
    // 콘솔에 출력하라 : 변수의 값을 디버깅시
    console.log ("학생 이름은 : <b>" + name + "<br>");
    console.log ("학생 나이는 : <b>" + age + " <br>");
}

